    public function index_data()
        {
        
        $module_title = $this->module_title;
        $module_name = $this->module_name;
        $module_path = $this->module_path;
        $module_icon = $this->module_icon;
        $module_model = $this->module_model;
        $module_name_singular = Str::singular($module_name);

        $module_action = 'List';

        $$module_name = $module_model::select('id','title','tag_line','icon','image','status','updated_at');

        $data = $$module_name;
       
        return DataTables::of($$module_name)
                        ->addColumn('action', function ($data) {
                            $module_name = $this->module_name;
                            return view('backend.includes.action_column', compact('module_name', 'data'));
                        })
                        ->editColumn('title', function ($data) {
                            return $data->title;
                        })
                        ->editColumn('tag_line', function ($data) {
                            return $data->tag_line;
                        })
                        ->addColumn('icon', function ($data) {
                            // $file= $data->getFirstMediaUrl('icon', 'thumb') ? $data->getFirstMediaUrl('icon', 'thumb') : '#';
                            return "<img src='$data->icon' width='60' >";
                        })
                        ->addColumn('image', function ($data) {
                            // $file= $data->getFirstMediaUrl('image', 'thumb') ? $data->getFirstMediaUrl('image', 'thumb') : '#';
                            return "<img src='$data->image' width='100' >";
                        })
                        
                        ->editColumn('status', function ($data) {
                            return  ($data->status ==1) ? "<span class='badge bg-success'>Published</span>" : '';
                        })
                        ->editColumn('updated_at', function ($data) {
                            return $data->updated_at->isoFormat('LLLL');
                        })
                        ->rawColumns(['name','image','icon', 'status', 'action'])
                        ->orderColumns(['id'], '-:column $1')
                        ->make(true);
    }

    /**
     * Select Options for Select 2 Request/ Response.
     *
     * @return Response
     */
    public function index_list(Request $request)
    {
        $module_title = $this->module_title;
        $module_name = $this->module_name;
        $module_path = $this->module_path;
        $module_icon = $this->module_icon;
        $module_model = $this->module_model;
        $module_name_singular = Str::singular($module_name);

        $module_action = 'List';

        $term = trim($request->q);

        if (empty($term)) {
            return response()->json([]);
        }

        $query_data = $module_model::where('name', 'LIKE', "%$term%")->published()->limit(10)->get();

        $$module_name = [];

        foreach ($query_data as $row) {
            $$module_name[] = [
                'id' => $row->id,
                'text' => $row->name,
            ];
        }

        return response()->json($$module_name);
    }

